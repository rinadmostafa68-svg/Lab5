    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        try {
        int id = Integer.parseInt(ID.getText());
        String name = Name.getText().trim();
        int age = Integer.parseInt(Age.getText());
        String gender = Gender.getSelectedItem().toString();
        String dept = Department.getText().trim();
        double gpa = Double.parseDouble(GPA.getText());

        if (name.isEmpty() || dept.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name and Department cannot be empty.");
            return;
        }

        Student updated = new Student(id, name, age, gender, dept, gpa);
        boolean success = StudentManager.updateStudent(updated);

        if (success) {
            JOptionPane.showMessageDialog(this, "Student updated successfully!");
            SearchActionPerformed(null); 
        } else {
            JOptionPane.showMessageDialog(this, "Student not found.");
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "add number for age-gpa-id .");
    }
    }//GEN-LAST:event_UpdateActionPerformed

