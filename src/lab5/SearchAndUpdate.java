    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        String keyword = Search.getText().trim();
        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter ID or Name to search.");
            return;
        }

        List<Student> results = StudentManager.searchStudent(keyword);
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        model.setRowCount(0); 

        for (Student s : results) {
            model.addRow(new Object[]{
                s.getId(), s.getName(), s.getAge(),
                s.getGender(), s.getDepartment(), s.getGpa()
            });
        }

        if (results.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No student found.");
        }
    }//GEN-LAST:event_SearchActionPerformed

